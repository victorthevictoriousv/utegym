<script>
import EventChat from '@/components/event/EventChat'

export default {
  props: ['event'],
  components: {
    EventChat
  },
  data() {
    return {
      share: false
    }
  },
  computed: {
    daysLeft() {
      let date = new Date

      const months = {
          'Januari': 'Jan',
          'Februari': 'Feb',
          'Mars': 'Mar',
          'April': 'Apr',
          'Maj': 'May',
          'Juni': 'Jun',
          'Juli': 'Jul',
          'Augusti': 'Aug',
          'September': 'Sep',
          'Oktober': 'Oct',
          'November': 'Now',
          'December': 'Dec'
      }

      let monthOfEvent = months[this.event.date.month]
      let deadline = new Date(`${monthOfEvent} ${this.event.date.day}, ${this.event.date.year}`).getTime(); 
      let now = new Date().getTime()

      let time = deadline - now

      let days = Math.floor(time / (1000 * 60 * 60 * 24)) + 1

      days = this.formatNumber(days)

      return days
    }
  },
  methods: {
    formatNumber(n) {
      return n > 9 ? "" + n: "0" + n;
    },
    formatName(n) {
      let formatedName = n.toLowerCase()
      formatedName = formatedName
        .replace(/ /g,"-")
        .replace(/å/g, 'a')
        .replace(/Å/g, 'a')
        .replace(/ä/g, 'a')
        .replace(/Ä/g, 'a')
        .replace(/ö/g, 'o')
        .replace(/Ö/g, 'o');
      return formatedName
    }
  }
}
